func TestTaskStatus_String(t *testing.T) {
	tests := []struct {
		status   TaskStatus
		expected string
	}{
		{TaskPending, "pending"},
		{TaskPlanning, "planning"},
		{TaskCoding, "coding"},
		{TaskTesting, "testing"},
		{TaskReviewing, "reviewing"},
		{TaskComplete, "complete"},
		{TaskFailed, "failed"},
		{TaskStatus(99), "unknown"},
	}
	for _, tt := range tests {
		if tt.status.String() != tt.expected {
			t.Errorf("TaskStatus(%d).String() = %q, want %q", tt.status, tt.status.String(), tt.expected)
		}
	}
}
