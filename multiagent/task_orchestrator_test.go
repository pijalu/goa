func TestParseTasks_Empty(t *testing.T) {
	tasks := ParseTasks("")
	if len(tasks) != 0 {
		t.Errorf("expected 0 tasks, got %d", len(tasks))
	}
}

func TestParseTasks_Single(t *testing.T) {
	tasks := ParseTasks("Refactor database layer")
	if len(tasks) != 1 {
		t.Fatalf("expected 1 task, got %d", len(tasks))
	}
	if tasks[0].Description != "Refactor database layer" {
		t.Errorf("expected 'Refactor database layer', got %q", tasks[0].Description)
	}
	if tasks[0].Status != TaskPending {
		t.Errorf("expected TaskPending status, got %v", tasks[0].Status)
	}
	if tasks[0].ID == "" {
		t.Error("expected non-empty task ID")
	}
}

func TestParseTasks_Multiple(t *testing.T) {
	tasks := ParseTasks("Build auth system, Add rate limiting, Write tests")
	if len(tasks) != 3 {
		t.Fatalf("expected 3 tasks, got %d", len(tasks))
	}
	if tasks[0].Description != "Build auth system" {
		t.Errorf("expected 'Build auth system', got %q", tasks[0].Description)
	}
	if tasks[1].Description != "Add rate limiting" {
		t.Errorf("expected 'Add rate limiting', got %q", tasks[1].Description)
	}
	if tasks[2].Description != "Write tests" {
		t.Errorf("expected 'Write tests', got %q", tasks[2].Description)
	}
}

func TestParseTasks_TrailingComma(t *testing.T) {
	tasks := ParseTasks("task1, task2, ")
	if len(tasks) != 2 {
		t.Errorf("expected 2 tasks (trailing comma trimmed), got %d", len(tasks))
	}
}

func TestParseTasks_TaskIDs_AutoGenerated(t *testing.T) {
	tasks := ParseTasks("A, B, C")
	if len(tasks) != 3 {
		t.Fatalf("expected 3 tasks, got %d", len(tasks))
	}
	for i, task := range tasks {
		expectedID := "task-1"
		if i == 1 {
			expectedID = "task-2"
		} else if i == 2 {
			expectedID = "task-3"
		}
		if task.ID != expectedID {
			t.Errorf("task %d: expected ID %q, got %q", i, expectedID, task.ID)
		}
	}
}

