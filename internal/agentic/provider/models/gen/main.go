// Command genmodels reads model definitions from JSON and generates models.generated.go.
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"strings"
	"text/template"
)

// ModelDef is the JSON schema for model definitions.
type ModelDef struct {
	ID             string            `json:"id"`
	Name           string            `json:"name"`
	API            string            `json:"api"`
	Provider       string            `json:"provider"`
	BaseURL        string            `json:"base_url,omitempty"`
	Reasoning      bool              `json:"reasoning"`
	ContextWindow  int               `json:"context_window"`
	MaxTokens      int               `json:"max_tokens"`
	InputTypes     []string          `json:"input_types"`
	ThinkingFormat string            `json:"thinking_format,omitempty"`
	Cost           ModelCostDef      `json:"cost,omitempty"`
	ThinkingLevels map[string]string `json:"thinking_levels,omitempty"`
}

type ModelCostDef struct {
	Input      float64 `json:"input,omitempty"`
	Output     float64 `json:"output,omitempty"`
	CacheRead  float64 `json:"cache_read,omitempty"`
	CacheWrite float64 `json:"cache_write,omitempty"`
}

// Input models.json, output models.generated.go.
const tmpl = `// Code generated by genmodels. DO NOT EDIT.
package models

import "github.com/pijalu/goa/internal/agentic/provider"

func init() {
    for _, m := range generatedModels {
        builtinModels[m.ID] = m
    }
}

var generatedModels = []provider.Model{
{{- range . }}
    {
        ID: "{{.ID}}",
        Name: "{{.Name}}",
        Api: provider.Api("{{.API}}"),
        Provider: provider.Provider("{{.Provider}}"),
        {{- if .BaseURL }}
        BaseURL: "{{.BaseURL}}",
        {{- end }}
        Reasoning: {{.Reasoning}},
        ContextWindow: {{.ContextWindow}},
        MaxTokens: {{.MaxTokens}},
        InputTypes: []string{ {{range .InputTypes}}"{{.}}", {{end}} },
        ThinkingFormat: provider.ThinkingFormat("{{.ThinkingFormat}}"),
        {{- if hasCost .Cost }}
        Cost: provider.ModelPricing{
            Input: {{.Cost.Input}},
            Output: {{.Cost.Output}},
            CacheRead: {{.Cost.CacheRead}},
            CacheWrite: {{.Cost.CacheWrite}},
        },
        {{- end }}
        {{- if .ThinkingLevels }}
        ThinkingLevelMap: provider.ThinkingLevelMap{
            {{- range $k, $v := .ThinkingLevels }}
            provider.ThinkingLevel("{{$k}}"): "{{$v}}",
            {{- end }}
        },
        {{- end }}
    },
{{- end }}
}
`

func main() {
	if len(os.Args) < 3 {
		fmt.Fprintf(os.Stderr, "Usage: genmodels <input.json> <output.go>\n")
		os.Exit(1)
	}

	inputPath := os.Args[1]
	outputPath := os.Args[2]

	data, err := os.ReadFile(inputPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error reading input: %v\n", err)
		os.Exit(1)
	}

	var models []ModelDef
	if err := json.Unmarshal(data, &models); err != nil {
		fmt.Fprintf(os.Stderr, "Error parsing JSON: %v\n", err)
		os.Exit(1)
	}

	funcMap := template.FuncMap{
		"hasCost": func(c ModelCostDef) bool {
			return c.Input != 0 || c.Output != 0 || c.CacheRead != 0 || c.CacheWrite != 0
		},
	}

	t := template.Must(template.New("models").Funcs(funcMap).Parse(tmpl))

	f, err := os.Create(outputPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error creating output: %v\n", err)
		os.Exit(1)
	}
	defer f.Close()

	if err := t.Execute(f, models); err != nil {
		fmt.Fprintf(os.Stderr, "Error executing template: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("Generated %s with %d models\n", outputPath, len(models))
}

